#string
name = "Daniel, esto es un string"
print(name, type(name))
#int
age = 30
print(age, type(age))
#float: numbers with decimal points
height = 1.75
print(height, type(height))
#boolean: Values are True or False
is_student = True
print(is_student, type(is_student))
#list: ordered and changeable collection, allows duplicate members
fruits = ["apple", "banana", "cherry"]
print(fruits, type(fruits))
#tuple: ordered and unchangeable collection, allows duplicate members
coordinates = (10.0, 20.0)
print(coordinates, type(coordinates))
#set: unordered and unindexed collection, no duplicate members
unique_numbers = {1, 2, 3, 4, 5}
print(unique_numbers, type(unique_numbers))
#dictionary: unordered, changeable, and indexed collection, no duplicate members
person = {"name": "Daniel", "age": 30}
print(person, type(person))
#none: represents the absence of a value
nothing = None
print(nothing, type(nothing))
